<?php

declare(strict_types=1);

namespace App\Organization\Domain\ValueObject;

/**
 * Визначає область видимості (scope) дозволу — які записи бачить/може змінювати працівник.
 *
 * Пріоритет (від найвужчого до найширшого):
 *   own(1) < subordinates(2) < subordinates_tree(3) < department(4) < department_tree(5) < organization(6)
 *
 * Якщо працівник має кілька ролей з різними scope для одного resource+action,
 * застосовується найширший scope.
 */
enum PermissionScope: string
{
    /**
     * Тільки власні записи.
     *
     * Працівник бачить/змінює лише ті записи, де він є суб'єктом.
     * Наприклад: employee/view/own — працівник бачить тільки свій профіль.
     */
    case Own = 'own';

    /**
     * Прямі підлеглі (1 рівень).
     *
     * Працівник бачить записи своїх прямих підлеглих (Employee.managerId = myId).
     * Не включає підлеглих підлеглих (тільки 1 рівень глибини).
     * Наприклад: employee/view/subordinates — менеджер бачить профілі тих, хто напряму йому підпорядковується.
     */
    case Subordinates = 'subordinates';

    /**
     * Все дерево підлеглих (всі рівні).
     *
     * Працівник бачить записи всіх підлеглих рекурсивно — прямих, їхніх підлеглих і т.д.
     * Використовує BFS по ланцюжку Employee.managerId.
     * Наприклад: employee/update/subordinates_tree — директор може редагувати профілі
     * всіх працівників у своїй вертикалі управління.
     */
    case SubordinatesTree = 'subordinates_tree';

    /**
     * Свій департамент (тільки поточний, без вкладених).
     *
     * Працівник бачить записи всіх працівників свого департаменту (Employee.departmentId = myDepartmentId).
     * Не включає вкладені (дочірні) підрозділи.
     * Наприклад: employee/view/department — HR-менеджер бачить всіх у своєму відділі.
     */
    case Department = 'department';

    /**
     * Свій департамент + все дерево дочірніх підрозділів.
     *
     * Працівник бачить записи працівників свого департаменту та всіх його
     * дочірніх департаментів (рекурсивно). Використовує materialized path (Department.path).
     * Наприклад: department/view/department_tree — керівник підрозділу бачить
     * свій підрозділ і всі вкладені (відділи, секції тощо).
     */
    case DepartmentTree = 'department_tree';

    /**
     * Вся організація — повний доступ без обмежень.
     *
     * Працівник бачить/змінює записи всіх працівників організації.
     * Зазвичай надається ролі Admin.
     * Наприклад: employee/manage/organization — адміністратор має повний доступ до всіх профілів.
     */
    case Organization = 'organization';
}
